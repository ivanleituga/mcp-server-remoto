const { getHomePage } = require("../utils/templates");
const { setupOAuthEndpoints } = require("./oauth_endpoints");
const { query, isConnected } = require("./database");
const sessionManager = require("./session_manager");
const { createMcpServer, toolsCount } = require("./mcp_server");
const { cleanupExpired } = require("./oauth_storage");
const { StreamableHTTPServerTransport } = require("@modelcontextprotocol/sdk/server/streamableHttp.js");
require("dotenv").config();

const express = require("express");
const cors = require("cors");
const crypto = require("crypto");

// ===============================================
// CONFIGURA√á√ÉO
// ===============================================

const app = express();
const PORT = process.env.PORT || 3000;
const SERVER_URL = process.env.RENDER_EXTERNAL_URL || `http://localhost:${PORT}`;

// ===============================================
// MIDDLEWARES
// ===============================================

app.use("/utils", express.static("utils"));
app.use(express.json({ limit: "50mb" }));
app.use(express.urlencoded({ extended: true, limit: "50mb" }));
app.use(cors({
  origin: true,
  credentials: true
}));

// Log de todas as requisi√ß√µes
app.use((req, _res, next) => {
  console.log(`üì® ${req.method} ${req.path}`);
  if (req.body && Object.keys(req.body).length > 0) {
    console.log("   Body keys:", Object.keys(req.body));
  }
  next();
});

// ===============================================
// CONFIGURAR OAUTH
// ===============================================

const { validateToken } = setupOAuthEndpoints(app);

// ===============================================
// CRIAR MCP SERVER
// ===============================================

const mcpServer = createMcpServer(query);

// ===============================================
// ROTAS B√ÅSICAS
// ===============================================

app.get("/", (_req, res) => {
  res.send(getHomePage(
    SERVER_URL, 
    isConnected(),
    sessionManager.count(),
    toolsCount
  ));
});

app.get("/health", async (_req, res) => {
  const dbStatus = isConnected();
  
  res.json({
    status: "ok",
    timestamp: new Date().toISOString(),
    database: dbStatus ? "connected" : "disconnected",
    sessions: sessionManager.count()
  });
});

// ===============================================
// MCP ENDPOINTS
// ===============================================

app.post("/mcp", validateToken, async (req, res) => {
  try {
    const sessionId = req.headers["x-session-id"] || crypto.randomUUID();
    
    console.log(`\nüì° POST /mcp - Session: ${sessionId}`);
    console.log(`   User: ${req.oauth.user}`);
    console.log(`   Client: ${req.oauth.client_id}`);
    
    let transport = sessionManager.get(sessionId);
    
    if (!transport) {
      console.log(`   üÜï Criando nova sess√£o: ${sessionId}`);
      
      transport = new StreamableHTTPServerTransport({
        request: req,
        response: res,
        sessionId
      });
      
      sessionManager.add(sessionId, transport);
      
      await mcpServer.connect(transport);
      
      console.log(`   ‚úÖ Sess√£o conectada: ${sessionId}`);
      console.log(`   üìä Total de sess√µes ativas: ${sessionManager.count()}`);
    } else {
      console.log(`   üîÑ Reutilizando sess√£o existente: ${sessionId}`);
      
      await transport.handleRequest(req, res);
    }
    
  } catch (error) {
    console.error("   ‚ùå Erro no MCP endpoint:", error.message);
    if (!res.headersSent) {
      res.status(500).json({
        error: "internal_error",
        message: error.message
      });
    }
  }
});

app.delete("/mcp", validateToken, async (req, res) => {
  const sessionId = req.headers["x-session-id"];
  
  console.log(`\nüóëÔ∏è  DELETE /mcp - Session: ${sessionId || "[SEM SESSION-ID]"}`);
  console.log(`   User: ${req.oauth.user}`);
  
  try {
    if (sessionId && sessionManager.exists(sessionId)) {
      const transport = sessionManager.get(sessionId);
      
      if (transport && transport.close) {
        await transport.close();
        console.log(`   ‚úÖ Transport fechado: ${sessionId}`);
      }
      
      sessionManager.remove(sessionId);
      console.log(`   ‚úÖ Sess√£o removida: ${sessionId}`);
      
      res.status(200).json({ 
        message: "Session deleted successfully",
        session_id: sessionId 
      });
    } else {
      console.log(`   ‚ö†Ô∏è  Sess√£o n√£o encontrada: ${sessionId}`);
      res.status(200).json({ 
        message: "Session not found or already deleted",
        session_id: sessionId 
      });
    }
    
  } catch (error) {
    console.error("   ‚ùå Erro ao deletar sess√£o:", error.message);
    res.status(500).json({
      error: "internal_error",
      message: error.message
    });
  }
});

